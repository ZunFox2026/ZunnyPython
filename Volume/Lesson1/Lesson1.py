# Bài 1: Cơ bản về Python
# Đây là một chương trình Python đơn giản

# In ra màn hình
print("Xin chào, thế giới!")

# Nhập dữ liệu từ người dùng
ten = input("Tên của bạn là gì? ")

# In ra thông tin vừa nhập
print("Xin chào, " + ten + "!")

# Đây là một biến số
so = 10

# In ra giá trị của biến
print("Giá trị của biến so là:", so)

# Đây là một biểu thức toán học
ket_qua = 5 + 3 * 2

# In ra kết quả của biểu thức
print("Kết quả của biểu thức là:", ket_qua)

# Đây là một danh sách (list)
ds = [1, 2, 3, 4, 5]

# In ra danh sách
print("Danh sách là:", ds)

# Đây là một từ điển (dictionary)
td = {"name": "John", "age": 30}

# In ra từ điển
print("Từ điển là:", td)